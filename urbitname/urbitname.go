func encodeWord(bytes []uint8)(word string){
  word = sisString[bytes[0]] + dexString[bytes[1]]
  return
}

func encodeWordQuad(bytes []uint8)(wordQuad string){
  wordQuad = encodeWord(bytes[0:2]) + "-" + encodeWord(bytes[2:4]) + "-" + encodeWord(bytes[4:6]) + "-" + encodeWord(bytes[6:8])
  return
}

// Encode returns the name in urbit encoding
func Encode(bytes []uint8) (name string) {
  if len(bytes) < 2 {
    name = dexString[bytes[0]]
    return
  }
  if len(bytes) < 4 {
    name = encodeWord(bytes[0:2])
    return
  }
  if len(bytes) < 8 {
    name = encodeWord(bytes[0:2]) + "-" + encodeWord(bytes[2:4])
    return
  }
  if len(bytes) < 16 {
    name = encodeWordQuad(bytes[0:8])
    return
  }
  name = encodeWordQuad(bytes[0:8])
  for i := 8; i < len(bytes) - 7; i += 8 {
    name = name + "--" + encodeWordQuad(bytes[i: i + 8])
  }
	return
}

func decodeWord(word string)(bytes []uint8, err error){
  bytes = make([]uint8, 2)
  if len(word) != 6 {
    err = errors.New(word + " is not 6 chars long")
    return
  }
  var prs bool
  bytes[0], prs = sisIndex[word[0:3]]
  if !prs {
    err = errors.New(word[0:3] + " is not found in sis")
    return
  }
  bytes[1], prs = dexIndex[word[3:6]]
  if !prs {
    err = errors.New(word[3:6] + " is not found in dex")
    return
  }
  return
}

func decodeWordQuad(wordQuad string)(bytes []uint8, err error){
  bytes = make([]uint8, 8)
  if len(wordQuad) != 27 {
    err = errors.New(wordQuad + " is not 27 chars long")
    return
  }
  words := strings.Split(wordQuad, "-")
  if len(words) != 4 {
    err = errors.New(wordQuad + " does not have 4 words")
    return
  }
  var pair []byte
  pair, err = decodeWord(words[0])
  if err != nil {
    return
  }
  bytes[0] = pair[0]
  bytes[1] = pair[1]
  pair, err = decodeWord(words[1])
  if err != nil {
    return
  }
  bytes[2] = pair[0]
  bytes[3] = pair[1]
  pair, err = decodeWord(words[2])
  if err != nil {
    return
  }
  bytes[4] = pair[0]
  bytes[5] = pair[1]
  pair, err = decodeWord(words[3])
  if err != nil {
    return
  }
  bytes[6] = pair[0]
  bytes[7] = pair[1]
  return
}

